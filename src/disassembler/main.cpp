ostream &operator<<(ostream &stream, const std::string &string)
{
	int i=0;
	do
	{
		stream << string[i];
		i++;
	}
	while(i < string.size() && string[i] != 0x00);

	return stream;
}

ostream &operator<<(ostream &stream, byte value)
{
	stream << "0x" << hex << (int)value;
	return stream;
}

int main(int argc, char **argv)
{
	showheader();

	if( argc < 3 )
	{
		showusage();

		return 1;
	}

	SpcFileLoader spcLoader(argv[1]);
	if( !spcLoader )
	{
		cerr << "Error while loading " << argv[1] << endl;
		cerr << "Please make sure the input file is a valid SPC file." << endl;

		return 1;
	}

	ofstream outputFile(argv[2]);
	if( !outputFile )
	{
		cerr << "Error while creating output stream for file " << argv[2] << endl;
		cerr << "Verify that you have writing rights in the output directory." << endl;

		return 1;
	}

	SpcFile spcFile = spcLoader.spcFile();

	// Generate header in result file
	outputFile << "# SPC700 Disassembler output generated by LegacySPC disassembler" << endl;
	outputFile << "# Song title: " << spcFile.id666Tag().songTitle << endl;
	outputFile << "# Game title: " << spcFile.id666Tag().gameTitle << endl;
	outputFile << "# PC: 0x" << hex << spcFile.processorRegisters().programCounter();
	outputFile << ", A: " << spcFile.processorRegisters().A();
	outputFile << ", X: " << spcFile.processorRegisters().X();
	outputFile << ", Y: " << spcFile.processorRegisters().Y();
	outputFile << ", PSW: " << spcFile.processorRegisters().programStatus();
	outputFile << ", SP: " << spcFile.processorRegisters().stackPointer();
	outputFile << endl << endl;

	int ramIndex = 0;
	
	do
	{
		byte readByte = spcFile.ramData()[ramIndex];

		stringstream resultLine;

		DisassemblerEntry entry = disassemblerTable[readByte];
		//if( entry.prettyOpcodeIndex != UNK_Pretty )
		//{
			// Print the RAM address
			resultLine << "0x" << hex << ramIndex << " ";

			// Print the opcode
			resultLine << prettyOpcodeName[entry.prettyOpcodeIndex];
		
			// Fetch the values (if any)
			queue<byte> values;
			int numBytesToFetch = entry.numBytes-1;
			
			for(int i=0; i<numBytesToFetch; i++)
			{
				values.push( spcFile.ramData()[++ramIndex] );
			}

			vector<string> arguments;
			// Add the arguments to the output
			for(int i=0; i<3; i++)
			{
				int argument = entry.args[i];

				switch(argument)
				{
					case Immediate_Argument:
					{
						int tempValue = static_cast<int>(values.front());
						values.pop();

						stringstream tempArg;
						tempArg << "#$" << hex << tempValue;

						arguments.push_back( tempArg.str() );
						break;
					}
					case Absolute_Argument:
					{
						int firstByte = static_cast<int>(values.front());
						values.pop();
						int secondByte = static_cast<int>(values.front());
						values.pop();

						word absValue;
						absValue.setLowByte(firstByte);
						absValue.setHighByte(secondByte);

						stringstream tempArg;
						tempArg << "$" << hex << absValue;
					
						arguments.push_back( tempArg.str() );
						break;
					}
					case AbsolutePlusX_Argument:
					{
						int firstByte = static_cast<int>(values.front());
						values.pop();
						int secondByte = static_cast<int>(values.front());
						values.pop();

						word absValue;
						absValue.setLowByte(firstByte);
						absValue.setHighByte(secondByte);

						stringstream tempArg;
						tempArg << "$" << hex << absValue << "+X";
					
						arguments.push_back( tempArg.str() );
						break;
					}
					case AbsolutePlusY_Argument:
					{
						int firstByte = static_cast<int>(values.front());
						values.pop();
						int secondByte = static_cast<int>(values.front());
						values.pop();

						word absValue;
						absValue.setLowByte(firstByte);
						absValue.setHighByte(secondByte);

						stringstream tempArg;
						tempArg << "$" << hex << absValue << "+Y";
					
						arguments.push_back( tempArg.str() );
						break;
					}
					case IndirectAbsolutePlusX_Argument:
					{
						int firstByte = static_cast<int>(values.front());
						values.pop();
						int secondByte = static_cast<int>(values.front());
						values.pop();

						word absValue;
						absValue.setLowByte(firstByte);
						absValue.setHighByte(secondByte);

						stringstream tempArg;
						tempArg << "[$" << hex << absValue << "+X]";
					
						arguments.push_back( tempArg.str() );
						break;
					}
					case IndirectDirectPagePlusX_Argument:
					{
						int tempValue = static_cast<int>(values.front());
						values.pop();

						stringstream tempArg;
						tempArg << "[0$" << hex << tempValue << "+X]";

						arguments.push_back( tempArg.str() );
						break;
					}
					case IndirectDirectPagePlusY_Argument:
					{
						int tempValue = static_cast<int>(values.front());
						values.pop();

						stringstream tempArg;
						tempArg << "[0$" << hex << tempValue << "]+Y";

						arguments.push_back( tempArg.str() );
						break;
					}
					case Relative_Argument:
					{
						offset tempValue = static_cast<offset>(values.front());
						values.pop();

						// Add the offset to the current ramIndex
						word realAddress = static_cast<word>(ramIndex + tempValue) + 1;

						stringstream tempArg;
						tempArg << "$" << hex << realAddress;

						arguments.push_back( tempArg.str() );
						break;
					}
					case DirectPage_Argument:
					{
						int tempValue = static_cast<int>(values.front());
						values.pop();

						stringstream tempArg;
						tempArg << "D$" << hex << tempValue;

						arguments.push_back( tempArg.str() );
						break;
					}
					case DirectPagePlusX_Argument:
					{
						int tempValue = static_cast<int>(values.front());
						values.pop();

						stringstream tempArg;
						tempArg << "D$" << hex << tempValue << "+X";

						arguments.push_back( tempArg.str() );
						break;
					}
					case DirectPagePlusY_Argument:
					{
						int tempValue = static_cast<int>(values.front());
						values.pop();

						stringstream tempArg;
						tempArg << "D$" << hex << tempValue << "+Y";

						arguments.push_back( tempArg.str() );
						break;
					}
					case Bit_Argument:
					{
						int tempValue = static_cast<int>(values.front());
						values.pop();

						stringstream tempArg;
						tempArg << tempValue;

						arguments.push_back( tempArg.str() );
						break;
					}
					case ComplementBit_Argument:
					{
						int tempValue = static_cast<int>(values.front());
						values.pop();

						stringstream tempArg;
						tempArg << "/" << tempValue;

						arguments.push_back( tempArg.str() );
						break;
					}
					case A_Argument:
						arguments.push_back( string("A") );
						break;
					case X_Argument:
						arguments.push_back( string("X") );
						break;
					case Y_Argument:
						arguments.push_back( string("Y") );
						break;
					case YA_Argument:
						arguments.push_back( string("YA") );
						break;
					case StackPointer_Argument:
						arguments.push_back( string("SP") );
						break;
					case ProgramStatusWard_Argument:
						arguments.push_back( string("PSW") );
						break;
					case IndirectX_Argument:
						arguments.push_back( string("(X)") );
						break;
					case IndirectXAutoIncrement_Argument:
						arguments.push_back( string("(X)+") );
						break;
					case IndirectY_Argument:
						arguments.push_back( string("(Y)") );
						break;
					case Carry_Argument:
						arguments.push_back( string("C") );
						break;
					default:
					{
						if( argument != -1 )
						{
							cerr << "Uncaught value: " << argument << endl;
						}
						break;
					}
				}
			}
		
			// Output arguments to result
			vector<string>::const_iterator it, itEnd = arguments.end();
			for(it = arguments.begin(); it != itEnd; ++it)
			{
				string temp = *it;
				resultLine << " " << temp;
				
				if( it+1 != itEnd )
				{
					resultLine << ",";
				}
			}

			outputFile << resultLine.str() << endl;
		//}

		ramIndex++;
	}
	while(ramIndex < 0xFFFF);

	outputFile.close();

	return 0;
}
